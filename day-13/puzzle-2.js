function summarise(map) {
  const numberOfRows = map.length;
  const numberOfColumns = map[0].length;
  findReflection: for (let column = 1; column < numberOfColumns; column++) {
    let errorCount = 0;
    for (let row = 0; row < numberOfRows; row++) {
      for (
        let left = column - 1, right = column;
        left >= 0 && right < numberOfColumns;
        left--, right++
      ) {
        if (map[row][left] !== map[row][right]) {
          errorCount++;
          if (errorCount > 1) continue findReflection;
        }
      }
    }
    if (errorCount === 1) return column;
  }

  findReflection: for (let row = 1; row < numberOfRows; row++) {
    let errorCount = 0;
    for (let column = 0; column < numberOfColumns; column++) {
      for (
        let top = row - 1, bottom = row;
        top >= 0 && bottom < numberOfRows;
        top--, bottom++
      ) {
        if (map[top][column] !== map[bottom][column]) {
          errorCount++;
          if (errorCount > 1) continue findReflection;
        }
      }
    }
    if (errorCount === 1) return row * 100;
  }
}
